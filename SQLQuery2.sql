SELECT DATEDIFF(YEAR, '2004', '2023') AS Year, 
	DATEDIFF(MONTH, '2004/01/14', '2023/07/13') AS Month,
	DATEDIFF(WEEK, '2004/01/14', '2023/07/13') AS Week,
	DATEDIFF(DAY, '2004/01/14', '2023/07/13') AS Day,
	DATEDIFF(HOUR, '2004/01/14 09:10', '2023/07/13 13:13') AS Hour,
	DATEDIFF(MINUTE, '2004/01/14 09:10', '2023/07/13 13:13') AS Minute,
	DATEDIFF(SECOND, '2004/01/14 09:10:30', '2023/07/13 13:13:55') AS Second;